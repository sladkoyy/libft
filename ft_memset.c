#include <string.h>

void	*ft_memset(void *dest, int c, size_t n)
{
	int i;

	i = 0;
	if (n == 0)
		return (dest);
	while (n-- > 0)
	{
		*((unsigned char*)dest + i++) = c;
	}
	return (dest);
}
