#include <string.h>

char	*ft_strcat(char *dest, const char *src)
{
	char *p;

	p = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0')
	{
		*dest++ = *src++;
	}
	*dest = '\0';
	return (p);
}
