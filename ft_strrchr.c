#include <string.h>

char	*ft_strrchr(const char *str, int c)
{
	int		i;
	char	*p;

	i = 0;
	p = NULL;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			p = ((char*)str + i);
		i++;
	}
	if (str[i] == '\0' && c == '\0')
		p = ((char*)str + i);
	return (p);
}
